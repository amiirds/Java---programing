
public class Entity {

    private String name;
    private String melli;

    public String getName() {
        return name;
    }
    public Entity setName(String name) {
        this.name = name;
        return this;
    }
    public String getMelli() {
        return melli;
    }
    public Entity setMelli(String melli) {
        this.melli = melli;
        return this;
    }
}